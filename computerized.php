        <strong><u>Caster:</u></strong><br />
Caster is used to describe the angle of a steering pivot, as seen from the side  of the vehicle and measured in degrees.&nbsp;<u>Caster alignment</u>&nbsp;plays a large role in evaluating the &quot;feel&quot; of  steering and the stability of high-speed transportation. Three to five degrees  of positive caster is typical for most vehicles, and lower angles for heavier  vehicles are used to keep steering comfortable. A faulty caster angle will  cause loose or difficult steering.<br />
<strong><u>Camber:</u></strong><br />
Camber is the angle of the wheel in relation to a vertical direction (seen from  the front or rear of the car). A negative camber measurement occurs when a  wheel leans toward the chassis; a positive measurement points the wheel away  from the car. An ideal camber angle assures optimal tire efficiency, proper  steering control, and a precautionary &quot;anti-roll&quot; directive that  engineers have adapted into vehicle designs to negate the effects of a body  roll. A faulty camber angle will create pulling and tire wear.<br />
<strong><u>Toe:</u></strong><br />
Like camber and caster, toe is measured by degrees and is another basic aspect  of suspension tuning. When a pair of wheels are placed with their front edges  pointed toward each other, the pair is defined as &quot;toe-ins.&quot; If the  front edges point away from each other, the pair is defined as  &quot;toe-outs.&quot; Essentially, a toe changes the distance between the front  and back of the rear tires, and a faulty toe angle will wear down your tires.</p>
